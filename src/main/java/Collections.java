import java.util.List;
import java.util.Map;
import java.util.Properties;
import java.util.Set;

public class Collections {
	
	
	private List elementList;
	private Set elementSet;
	private Map elementMap;
	private Properties elementProperties;
	
	public List getElementList() {
		return elementList;
	}
	
	public void setElementList(List elementList) {
		this.elementList = elementList;
	}
	public Set getElementSet() {
		return elementSet;
	}
	
	public void setElementSet(Set elementSet) {
		this.elementSet = elementSet;
	}
	
	public Map getElementMap() {
		return elementMap;
	}
	
	public void setElementMap(Map elementMap) {
		this.elementMap = elementMap;
	}
	
	public Properties getElementProperties() {
		return elementProperties;
	}
	
	public void setElementProperties(Properties elementProperties) {
		this.elementProperties = elementProperties;
	}
	
	
	

}
